def show_predictor():
    st.title("Delivery Risk Predictor")
    st.write("Enter the delivery details below to predict the failure risk "
             "**before the driver leaves the depot.**")

    # --- Input widgets, arranged in two columns ---
    col1, col2 = st.columns(2)

    with col1:
        city_zone = st.selectbox("City Zone",
            ["urban_core", "urban_inner", "suburban", "rural"])
        time_window = st.selectbox("Time Window",
            ["morning_08_12", "afternoon_12_17", "evening_17_21", "anytime"])
        weather = st.selectbox("Weather",
            ["clear", "cloudy", "rain", "snow"])
        attempt_number = st.selectbox("Attempt Number", [1, 2, 3])
        driver_experience_yrs = st.slider("Driver Experience (years)", 0.5, 20.0, 5.0)

    with col2:
        package_count = st.slider("Package Count", 1, 12, 3)
        weight_kg = st.slider("Total Weight (kg)", 0.3, 35.0, 5.0)
        distance_km = st.slider("Distance from Depot (km)", 0.5, 60.0, 10.0)
        stops_before_this = st.slider("Stops Before This One", 0, 40, 10)
        traffic_index = st.slider("Traffic Index (1=clear, 10=gridlock)", 1.0, 10.0, 5.0)

    # --- Predict button ---
    if st.button("Predict Risk", type="primary"):

        # Build a single-row input with the same columns as training
        raw_input = {
            "city_zone": city_zone,
            "time_window": time_window,
            "weather": weather,
            "attempt_number": attempt_number,
            "driver_experience_yrs": driver_experience_yrs,
            "package_count": package_count,
            "weight_kg": weight_kg,
            "distance_km": distance_km,
            "stops_before_this": stops_before_this,
            "traffic_index": traffic_index,
        }

        # Encode it exactly like the training data
        input_df = pd.DataFrame([raw_input])
        input_encoded = pd.get_dummies(input_df)
        input_encoded = input_encoded.reindex(columns=model_columns, fill_value=0)

        # Get the failure probability (probability of class 0 = failed)
        classes = list(model.classes_)
        fail_index = classes.index(0)
        failure_risk = model.predict_proba(input_encoded)[0][fail_index] * 100

        # --- Show the result ---
        st.markdown("---")
        st.subheader("Prediction Result")

        # Color the result based on risk level
        if failure_risk >= 60:
            st.error(f"⚠️ HIGH RISK — {failure_risk:.0f}% chance of failure")
            st.write("**Recommendation:** Consider rescheduling, confirming the "
                     "customer is home, or assigning an experienced driver.")
        elif failure_risk >= 35:
            st.warning(f"⚡ MEDIUM RISK — {failure_risk:.0f}% chance of failure")
            st.write("**Recommendation:** Monitor this delivery and have a backup plan.")
        else:
            st.success(f"✅ LOW RISK — {failure_risk:.0f}% chance of failure")
            st.write("**Recommendation:** Proceed as normal.")

        # Show the risk as a progress bar
        st.progress(int(failure_risk))


# ============================================================
# Show the selected page
# ============================================================

if page == "Overview":
    show_overview()
else:
    show_predictor()