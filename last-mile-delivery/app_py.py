import streamlit as st
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import joblib

# --- Corporate colors (same as our charts) ---
DARK_BLUE = "#003366"
MID_BLUE = "#0066CC"
AMBER = "#E8A000"
RED = "#CC0000"
GREY = "#6C757D"


# ============================================================
# Load data and model (cached so it only loads once)
# ============================================================

# @st.cache_data tells Streamlit to remember the result so the
# app stays fast and doesn't reload the file on every click.
@st.cache_data
def load_data():
    return pd.read_csv("data/last_mile_deliveries_clean.csv")

@st.cache_resource
def load_model():
    model = joblib.load("data/delivery_model.pkl")
    columns = joblib.load("data/model_columns.pkl")
    return model, columns

df = load_data()
model, model_columns = load_model()


# ============================================================
# Sidebar - page navigation
# ============================================================

st.sidebar.title("🚚 Last Mile Delivery")
page = st.sidebar.radio("Go to:", ["Overview", "Risk Predictor"])

st.sidebar.markdown("---")
st.sidebar.caption("Built by Chandana Nuthangi")
st.sidebar.caption("from-floor-to-data portfolio")


