def show_overview():
    st.title("Delivery Performance Overview")
    st.write("Understanding where and why last mile deliveries fail.")

    # --- Top row: key numbers (KPIs) ---
    total = len(df)
    failure_rate = (1 - df["delivery_success"].mean()) * 100
    avg_delay = df["delay_minutes"].mean()

    col1, col2, col3 = st.columns(3)
    col1.metric("Total Deliveries", f"{total:,}")
    col2.metric("Failure Rate", f"{failure_rate:.1f}%")
    col3.metric("Avg Delay", f"{avg_delay:.0f} min")

    st.markdown("---")

    # --- Chart 1: Failure rate by zone ---
    st.subheader("Failure Rate by City Zone")
    zone_fail = df.groupby("city_zone")["delivery_success"].apply(
        lambda x: (1 - x.mean()) * 100
    ).sort_values(ascending=False)

    fig, ax = plt.subplots(figsize=(8, 4))
    colors = [RED, AMBER, MID_BLUE, DARK_BLUE]
    ax.bar(zone_fail.index, zone_fail.values, color=colors)
    ax.set_ylabel("Failure Rate (%)")
    for i, v in enumerate(zone_fail.values):
        ax.text(i, v + 0.5, f"{v:.1f}%", ha="center", fontweight="bold")
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    st.pyplot(fig)

    # --- Chart 2: Failure rate by weather ---
    st.subheader("Failure Rate by Weather")
    weather_fail = df.groupby("weather")["delivery_success"].apply(
        lambda x: (1 - x.mean()) * 100
    ).sort_values(ascending=False)

    fig2, ax2 = plt.subplots(figsize=(8, 4))
    ax2.bar(weather_fail.index, weather_fail.values, color=MID_BLUE)
    ax2.set_ylabel("Failure Rate (%)")
    for i, v in enumerate(weather_fail.values):
        ax2.text(i, v + 0.5, f"{v:.1f}%", ha="center", fontweight="bold")
    ax2.spines["top"].set_visible(False)
    ax2.spines["right"].set_visible(False)
    st.pyplot(fig2)

    # --- Chart 3: Failure rate by attempt number ---
    st.subheader("Failure Rate by Attempt Number")
    attempt_fail = df.groupby("attempt_number")["delivery_success"].apply(
        lambda x: (1 - x.mean()) * 100
    )

    fig3, ax3 = plt.subplots(figsize=(8, 4))
    ax3.bar(["1st", "2nd", "3rd"], attempt_fail.values, color=[DARK_BLUE, AMBER, RED])
    ax3.set_ylabel("Failure Rate (%)")
    for i, v in enumerate(attempt_fail.values):
        ax3.text(i, v + 0.5, f"{v:.1f}%", ha="center", fontweight="bold")
    ax3.spines["top"].set_visible(False)
    ax3.spines["right"].set_visible(False)
    st.pyplot(fig3)


